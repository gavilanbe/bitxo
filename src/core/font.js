function fmt(n){
  n = Math.floor(n);
  if(n<1000) return String(n);
  if(n<1e6) return (n/1e3).toFixed(1).replace('.0','')+'K';
  if(n<1e9) return (n/1e6).toFixed(1).replace('.0','')+'M';
  return (n/1e9).toFixed(1).replace('.0','')+'B';
}
